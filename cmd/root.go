package cmd

import (
	"os"

	"github.com/spf13/cobra"
)

// rootCmd represents the base command when called without any subcommands
var rootCmd = &cobra.Command{
	Use:   "family-feud",
	Short: "Play this popular TV show in a cozy shell",
	Long: `This application is based on the popular TV show within a shell.

This game is meant to be played with an animator and a config file containing questions and answers.
`,
}

func Execute() {
	err := rootCmd.Execute()
	if err != nil {
		os.Exit(1)
	}
}

func init() {
	rootCmd.Flags().BoolP("toggle", "t", false, "Help message for toggle")
}
